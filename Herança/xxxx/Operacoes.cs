class Operacoes
{
    public Operacoes()
    {
    }
    internal void Somar(int x, int y)
    {
        Console.WriteLine(x+y);
    }

    public void subtracao(int x, int y)
    {
        Console.WriteLine(x-y);
    }

    public void Dividir(int x , int y)
    {
        Console.WriteLine(x/y);
    }

    public void Multiplicar(int x , int y)
    {
        Console.WriteLine(x*y);
    }

}